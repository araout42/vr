#include "libft.h"
#include <limits.h>

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n == INT_MIN)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar('0' + n % 10);
}
